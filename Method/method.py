list = [2,1 ,3,5,4]
list.sort() # sort the list in ascending order
print(list)
list.sort(reverse=True) # sort the list in descending order
print(list)
list.reverse()
print(list)
list.append(6) # add 6 at the end of list
print(list)
list.insert(0, 0) # add 0 at the 0th index of
print(list)
list.remove(3) # remove 3 from the list
print(list)
list.pop(3) # remove the element at index 3 from the list
